# INPUTS --> speed --> number (integer)
# OUTPUTS --> answer safe or not --> boolean

# analyze speed
# if speed > 60 or speed < 40 then say true
# otherwise say false 



def unsafe?(speed)
  if speed == < 40 || speed > 60
    true
  else
    false
  end
end


def not_safe?(speed)
	speed == < 40 || speed > 60 ? true : false
end
	


